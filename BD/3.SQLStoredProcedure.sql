CREATE OR ALTER PROCEDURE [dbo].[uspGetTeachers]
@CourseId INT
AS
BEGIN
   SET NOCOUNT ON;
   SELECT 
   D.ID_DOCENTE AS TeacherId,
   D.CODIGO_DOCENTE AS Code,
   D.NOMBRE + ' ' + D.APELLIDO AS Name,
   CAST(AVG(C.PUNTAJE_PROMEDIO) AS DECIMAL(8,1)) AS QualificationTotal
   FROM dbo.DOCENTE D
   INNER JOIN dbo.DOCENTE_CURSO DC ON DC.ID_DOCENTE = D.ID_DOCENTE
   INNER JOIN dbo.CALIFICACION C ON C.ID_DOCENTE = D.ID_DOCENTE
   WHERE DC.ID_CURSO = @CourseId
   GROUP BY  D.ID_DOCENTE, D.CODIGO_DOCENTE, D.NOMBRE + ' ' + D.APELLIDO
END
GO

