GO

CREATE OR ALTER PROCEDURE [dbo].[uspGetTeachersDetail]
@TeacherId INT
AS
BEGIN
   SET NOCOUNT ON;
   SELECT 
   CD.SECUENCIA_CUESTIONARIO AS SequenceQuestion,
   CU.DESCRIPCION AS Description,
   CAST(CAST(SUM(CD.PUNTAJE) AS DECIMAL(8,1))/CAST(COUNT(CD.PUNTAJE) AS DECIMAL(8,1)) AS DECIMAL(8,1)) AS QuestionScoreTotal
   FROM dbo.CALIFICACION_DET CD 
   INNER JOIN dbo.CALIFICACION C ON C.ID_CALIFICACION = CD.ID_CALIFICACION
   INNER JOIN dbo.CUESTIONARIO CU ON CU.NRO_CUESTIONARIO = CD.NRO_CUESTIONARIO AND CU.SECUENCIA = CD.SECUENCIA_CUESTIONARIO
   WHERE C.ID_DOCENTE = @TeacherId
   GROUP BY CD.SECUENCIA_CUESTIONARIO, CU.DESCRIPCION
   ORDER BY CD.SECUENCIA_CUESTIONARIO
END
GO

